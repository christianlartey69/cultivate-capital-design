import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, adminContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are an AI assistant for the CES Crowdfarming admin panel. You help administrators manage the platform, analyze data, and make informed decisions.

Platform Statistics:
- Total Investments: GHS ${adminContext?.totalInvestments?.toLocaleString() || "0"}
- Active Investments: ${adminContext?.activeInvestments || 0}
- Total Users: ${adminContext?.totalUsers || 0}
- Pending Verifications: ${adminContext?.pendingVerifications || 0}

You can help with:
- Analyzing investment trends and user behavior
- Summarizing farm activity and performance
- Identifying issues in user accounts or investments
- Suggesting improvements to farm cycles and packages
- Generating insights from investment data
- Detecting unusual patterns or behaviors
- Drafting customer support responses
- Managing farm visit schedules and availability

Admin Capabilities:
- Upload and manage asset videos for specific users
- Verify user identities and documentation
- Update investment statuses and ROI amounts
- Assign unique tag IDs to assets
- Manage farm visit bookings and confirmations
- Control package availability and pricing
- Send notifications to users

Be analytical, precise, and focus on actionable insights. Provide data-driven recommendations and flag any anomalies.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limits exceeded, please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Admin chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});